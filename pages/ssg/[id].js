export async function getStaticPaths() {
  const resp = await fetch("https://demo-be-thrive.herokuapp.com/products");
  const data = await resp.json();

  const allPaths = data.map((product) => ({ params: { id: `${product.id}` } }));

  return {
    paths: allPaths,
    fallback: false, // false or 'blocking'
  };
}

