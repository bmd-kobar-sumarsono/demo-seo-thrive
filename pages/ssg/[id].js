export async function getStaticProps(context) {
  const { query, params } = context;
  const resp = await fetch(
    `https://demo-be-thrive.herokuapp.com/products?id=${params.id}`
  );
  const data = await resp.json();
  const description = data[0]?.description;
  const heroID = query?.hero || 0;
  const villainID = query?.villain || 0;
  const requiredData = data[0];
  const seos = {
    title: requiredData.title,
    description: requiredData.description,
    author: "Alta Thrive",
    keywords: requiredData.title,
    canonicalUrl: `https://demo-seo-thrive.vercel.app/ssg/${params.id}`,
    ogTitle: requiredData.title,
    ogDescription: requiredData.description,
    ogImageUrl: `https://demo-be-thrive.herokuapp.com${requiredData.image.url}`,
    ogImageWidth: 1200,
    ogImageHeight: 630,
    ogType: "product",
    ogUrl: `https://demo-seo-thrive.vercel.app/ssg/${params.id}`,
    twitterCard: "summary",
    twitterTitle: requiredData.title,
    twitterDescription: requiredData.description,
    twitterUrl: `https://demo-seo-thrive.vercel.app/ssg/${params.id}`,
  };

  return {
    props: {
      description,
      heroID,
      villainID,
      seos,
    }, // will be passed to the page component as props
    revalidate: 5,
  };
}
